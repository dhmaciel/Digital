package com.digital.bean;

public class LoginBean {
	
	private String login;
	private String senha;
	private Boolean isClienteSelected;
	
	public String getLogin() {
		return login;
	}
	public void setLogin(String login) {
		this.login = login;
	}
	public String getSenha() {
		return senha;
	}
	public void setSenha(String senha) {
		this.senha = senha;
	}
	public Boolean getIsClienteSelected() {
		return isClienteSelected;
	}
	public void setIsClienteSelected(Boolean isClienteSelected) {
		this.isClienteSelected = isClienteSelected;
	}

}
